def get_report(generation_results: dict) -> dict:
    """
    –§–æ—Ä–º–∏—Ä—É–µ—Ç –æ–ø–∏—Å–∞–Ω–∏—è –≤–∞–∫–∞–Ω—Å–∏–∏ –≤ —Ä–∞–∑–ª–∏—á–Ω—ã—Ö —Ñ–æ—Ä–º–∞—Ç–∞—Ö: –¥–ª—è —Å–∞–π—Ç–∞, —Ñ–ª–∞–µ—Ä–∞, –¢–í/–≥–∞–∑–µ—Ç—ã, —Å–æ—Ü—Å–µ—Ç–µ–π.

    Parameters:
        generation_results (dict): –°–ª–æ–≤–∞—Ä—å —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏.

    Returns:
        dict: –°–ª–æ–≤–∞—Ä—å —Å –∫–ª—é—á–∞–º–∏ job_site, job_flyer_format, job_media_format, job_social_media_format
    """
    report = {}

    # --- –†–∞–±–æ—Ç–Ω—ã–π —Å–∞–π—Ç ---
    try:
        # –ü–æ–ª—É—á–∞–µ–º –æ–ø–∏—Å–∞–Ω–∏–µ –≤–∞–∫–∞–Ω—Å–∏–∏ –∏ –ø–æ–∑–∏—Ü–∏—é
        jd = generation_results["job_description"]["structured_job_description"]
        position = jd.get('position').strip()

         # –ü–æ–ª—É—á–∞–µ–º –∑–∞–¥–∞—á–∏ –∏ —Ç—Ä–µ–±–æ–≤–∞–Ω–∏—è
        responsibilities = jd.get('responsibilities', {})
        tasks = [responsibilities.get(f"task_{i}") for i in range(1, 6)]
        tasks = [t for t in tasks if t]
    
        requirements_dict = jd.get('requirements', {})
        requirements = [requirements_dict.get(f"requirement_{i}") for i in range(1, 6)]
        requirements = [r for r in requirements if r]
        
        # –°–æ–±–∏—Ä–∞–µ–º –æ–ø–∏—Å–∞–Ω–∏–µ –≤–∞–∫–∞–Ω—Å–∏–∏
        job_site_template = f"""
        –í–∞–∫–∞–Ω—Å–∏—è: **{position}**
        
        **–ú—ã –ø—Ä–µ–¥–ª–∞–≥–∞–µ–º**
        
        - –†–∞–±–æ—Ç—É –≤ –∫—Ä—É–ø–Ω–µ–π—à–µ–π —Ç—Ä–∞–Ω—Å–ø–æ—Ä—Ç–Ω–æ-–ª–æ–≥–∏—Å—Ç–∏—á–µ—Å–∫–æ–π –∫–æ–º–ø–∞–Ω–∏–∏ –≤ –†–æ—Å—Å–∏–∏
        - –û—Ñ–∏—Ü–∏–∞–ª—å–Ω–æ–µ –æ—Ñ–æ—Ä–º–ª–µ–Ω–∏–µ, —Å–æ—Ü–∏–∞–ª—å–Ω—ã–π –ø–∞–∫–µ—Ç
        - –î–ú–°, –≤–∫–ª—é—á–∞—è —Å—Ç–æ–º–∞—Ç–æ–ª–æ–≥–∏—é
        - –ê–∫—Ç–∏–≤–Ω—É—é –∫–æ—Ä–ø–æ—Ä–∞—Ç–∏–≤–Ω—É—é –∂–∏–∑–Ω—å, —Å–∫–∏–¥–æ—á–Ω—É—é —Å–∏—Å—Ç–µ–º—É –æ—Ç –ø–∞—Ä—Ç–Ω–µ—Ä–æ–≤
        - –ö–æ—Ä–ø–æ—Ä–∞—Ç–∏–≤–Ω–æ–µ –æ–±—É—á–µ–Ω–∏–µ
        - –ì—Ä–∞—Ñ–∏–∫ —Ä–∞–±–æ—Ç—ã:
        
        **–û—Å–Ω–æ–≤–Ω—ã–µ –∑–∞–¥–∞—á–∏**
        """

        job_site_template += "".join(f"- {task}\n" for task in tasks)
        
        job_site_template += "\n**–ú—ã –æ–∂–∏–¥–∞–µ–º**\n\n"
        job_site_template += "".join(f"- {req}\n" for req in requirements)

        report["job_site"] = job_site_template.strip()
    except Exception as e:
        report["job_site"] = f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏–∏ –æ–ø–∏—Å–∞–Ω–∏—è –≤–∞–∫–∞–Ω—Å–∏–∏: {e}"

    # --- –§–ª–∞–µ—Ä ---
    try:
        flyer: FlyerFormat = generation_results["job_formats"]["flyer_data"]
        flyer_template = (
            f"{flyer.get('company_info')}\n\n"
            f"**–ü–æ–∑–∏—Ü–∏—è:** {position} \n\n"
            f"üöÄ **–û —Ä–∞–±–æ—Ç–µ:**\n{flyer.get('job_details')}\n\n"
            f"üîç **–û –∫–∞–Ω–¥–∏–¥–∞—Ç–µ:**\n{flyer.get('requirements')}\n\n"
            f"üí∞ **–í–æ–∑–Ω–∞–≥—Ä–∞–∂–¥–µ–Ω–∏–µ:**  –ø–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º —Å–æ–±–µ—Å–µ–¥–æ–≤–∞–Ω–∏—è\n\n"
            "–ï—Å–ª–∏ –≤—ã –≥–æ—Ç–æ–≤—ã –ø—Ä–∏—Å–æ–µ–¥–∏–Ω–∏—Ç—å—Å—è –∫ –Ω–∞—à–µ–π –∫–æ–º–∞–Ω–¥–µ –∏ –ø–æ–ø—Ä–æ–±–æ–≤–∞—Ç—å —Å–≤–æ–∏ —Å–∏–ª—ã –≤ —Ä–µ–∞–ª–∏–∑–∞—Ü–∏–∏ –º–∞—Å—à—Ç–∞–±–Ω—ã—Ö –ø—Ä–æ–µ–∫—Ç–æ–≤, –ø–∏—à–∏ –≤ Telegram: @hr_contact"
        )
        report["job_flyer_format"] = flyer_template.strip()
    except Exception as e:
        report["job_flyer_format"] = f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏–∏ —Ñ–ª–∞–µ—Ä–∞: {e}"

    # --- –ì–∞–∑–µ—Ç–∞ / –¢–í ---
    try:
        media: MediaFormat = generation_results["job_formats"]["media_data"]
        media_template = (
            f"**–í–∞–∫–∞–Ω—Å–∏—è:** {media.get('job_title')}\n\n"
            "**–ó–∞—Ä–ø–ª–∞—Ç–∞:**\n"
            f"**–ö–ª—é—á–µ–≤—ã–µ –∑–∞–¥–∞—á–∏:** {media.get('key_function')}\n\n"
            f"**–ö–æ–Ω—Ç–∞–∫—Ç—ã:** cv@fesco.com"
        )
        report["job_media_format"] = media_template.strip()
    except Exception as e:
        report["job_media_format"] = f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏–∏ –æ–ø–∏—Å–∞–Ω–∏—è –¥–ª—è –º–µ–¥–∏–∞: {e}"

    # --- –°–æ—Ü–∏–∞–ª—å–Ω—ã–µ —Å–µ—Ç–∏ ---
    try:
        social: SocialMediaFormat = generation_results["job_formats"][
            "social_media_data"
        ]
        social_template = (
            f"üè¢ **{social.get('company_info')}**\n\n"
            "üìã **–û —Ä–∞–±–æ—Ç–µ –∏ –æ–±—è–∑–∞–Ω–Ω–æ—Å—Ç—è—Ö:**\n"
            f"{social.get('project_info')}\n\n"
            f"‚úÖ **–¢—Ä–µ–±–æ–≤–∞–Ω–∏—è:**\n{social.get('key_requirements')}\n\n"
            "üí∞ **–í–æ–∑–Ω–∞–≥—Ä–∞–∂–¥–µ–Ω–∏–µ:**\nüìç **–õ–æ–∫–∞—Ü–∏—è:**\n\n"
            "üìû **–ö–æ–Ω—Ç–∞–∫—Ç—ã:** cv@fesco.com\n\n" + " ".join(social.get('hashtags', []))
        )
        report["job_social_media_format"] = social_template.strip()
    except Exception as e:
        report["job_social_media_format"] = (
            f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏–∏ –æ–ø–∏—Å–∞–Ω–∏—è –¥–ª—è —Å–æ—Ü—Å–µ—Ç–µ–π: {e}"
        )

    return report


